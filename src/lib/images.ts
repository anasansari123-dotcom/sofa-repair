export const GALLERY_ALL_IMAGES: string[] = (() => {
  const diningFeature = [
    "/wall-panels/dining/D1.png",
    "/wall-panels/dining/D2.png",
    "/wall-panels/dining/D3.png",
    "/wall-panels/dining/D4.png",
    "/wall-panels/dining/D5.png",
    "/wall-panels/dining/D6.png",
  ];
  const picks = GALLERY_CATEGORIES.filter((cat) => cat.slug !== "dining-area").map((cat) =>
    cat.images.filter((src) => !diningFeature.includes(src)).slice(0, 2)
  );
  const mixed: string[] = [...diningFeature];
  const max = Math.max(0, ...picks.map((p) => p.length));
  for (let i = 0; i < max; i++) {
    for (const group of picks) {
      if (group[i]) mixed.push(group[i]);
    }
  }
  return mixed;
})();

